public class Expressions {

    public static void main() {

        // Expressions = operands & operators
        // operands = values, variables, constants, etc.
        // operators = +, -, *, /, %, ++, --, etc.

        int friends = 10;

        friends++; // would be 11

        System.out.println(friends);

        int enemies = 10;

        enemies--; // would be 9

        System.out.println(enemies);

    }

}
